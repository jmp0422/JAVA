package com.multi.b_conditional;

public class ex02 {
    public static void main(String[] args) {
        String data = "OK";

        if (data.equals("OK")) {
            System.out.println("OK입니다.");
        } else if (data.equals("NO")) {
            System.out.println("NO입니다.");
        } else {
            System.out.println("모르겠습니다.");
        }
    }
}
